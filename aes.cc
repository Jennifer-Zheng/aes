#include <iostream>
#include <fstream>
#include <string>
#include <vector>
#include <bitset>
#include "./aes.h"

int main(int argc, char **argv) {
    int key_size = std::stoi(argv[2]);
    std::string key_file_str(argv[4]);
    std::string input_file_str(argv[6]);
    std::string output_file_str(argv[8]);
    std::string mode = argv[10];
    std::ifstream key_file;

    std::vector<unsigned char> key;

    if (key_size != 256 && key_size != 128) {
        std::cout << "Invalid Key Size: " << key_size << std::endl;
        std::cout << "Only 128 or 256 bit keys are supported" << std::endl;
        return 0;
    }

    // Obtain key file
    key_file.open(key_file_str, std::ios::binary);
    if (key_file.is_open()) {
        char byte;
        int key_bytes = 0;;

        while (key_file.get(byte)) {
            key.push_back((unsigned char)byte);
            ++key_bytes;
        }

        if ((8 * key_bytes) != key_size) {
            std::cout << "Your key is not " << key_size << "bits long " << std::endl;
            std::cout << "Your key is " << (8 * key_bytes) << "bits long " << std::endl;
            return 0;
        }
    } else {
        std::cout << "Error with opening the key file" << std::endl;
        return 0;
    }

    // Opens a file as binary
    std::ifstream file;
    std::vector<std::vector<std::vector<unsigned char>>> state;
    int num_bytes = 0;
    file.open(input_file_str, std::ios::binary);
    if (file.is_open()) {
        char byte;
        int matrix_pos = 0;
        int matrix_index = -1;
        // List of 4x4 Matrices

        while (file.get(byte)) {
            // Keeps track of the position in the matrix the next byte should be
            // If it is 0, it means we should start a new matrix
            if (matrix_pos == 0) {
                std::vector<std::vector<unsigned char>> matrix(4, std::vector<unsigned char> (4, 0));
                state.push_back(matrix);
                // Update the current matrix
                ++matrix_index;
            }

            // Converts the matrix_pos range from [0-15] to row/column indexing
            // Makes this column-order instead of row-order
            int row = matrix_pos % 4;
            int col = matrix_pos / 4;

            state[matrix_index][row][col] = (unsigned char)byte;

            // Updates the next position in the matrix
            // and the number of bytes collectd
            matrix_pos = (matrix_pos+1) % 16;
            ++num_bytes;
        }
        file.close();

        // If we are encrypting, make sure that we pad to a factor of 16 bytes
        if (mode == "encrypt") {
            int rem = num_bytes % 16;
            if (rem == 0) {
                // If we recieved a factor of 16 bytes, we add a new padding matrix
                std::vector<std::vector<unsigned char>> matrix(4, std::vector<unsigned char> (4, 16));
                state.push_back(matrix);
            } else {
                // Otherwise, we pad the rest of the current matrix
                for (int i = matrix_pos; i < 16; ++i) {
                    int row = i % 4;
                    int col = i / 4;
                    state[matrix_index][row][col] = (16-rem);
                }
            }
        }

        // Calculate number of rounds for Rijndael's algorithm
        int n_r = 0;
        if (key_size == 128) {
            n_r = 10;
        } else if (key_size == 256) {
            n_r = 14;
        }

        // Obtain the key schedule from the original key
        std::vector<std::vector<unsigned char>> key_schedule = KeyExpansion(key, key_size);

        // Rijndael's Algorithm
        if (mode == "encrypt") {
            AddRoundKey(state, key_schedule, 0);
            for (int r = 1; r <= (n_r - 1); ++r) {
                SubBytes(state);
                ShiftRows(state);
                MixColumns(state);
                AddRoundKey(state, key_schedule, r);
            }
            SubBytes(state);
            ShiftRows(state);
            AddRoundKey(state, key_schedule, n_r);
        } else if (mode == "decrypt") {
            AddRoundKey(state, key_schedule, n_r);
            for (int r = (n_r - 1); r >= 1; --r) {
                InverseShiftRows(state);
                InverseSubBytes(state);
                AddRoundKey(state, key_schedule, r);
                InverseMixColumns(state);
            }
            InverseShiftRows(state);
            InverseSubBytes(state);
            AddRoundKey(state, key_schedule, 0);
        }

        // Create Output File
        // If we are decrypting, find the number of pads so we don't write them
        int num_pads = 0;
        if (mode == "decrypt") {
            num_pads = state[state.size()-1][3][3];
        }
        int bytes_to_write = num_bytes - num_pads;
        int num_bytes_written = 0;

        std::ofstream output(output_file_str, std::ios::binary);
        for (int i = 0; i < state.size(); ++i) {
            for (int c = 0; c < 4; ++c) {
                for (int r = 0; r < 4; ++r) {
                    if (mode == "decrypt" && num_bytes_written >= bytes_to_write) {
                        break;
                    }
                    char byte = state[i][r][c];
                    output.put(byte);
                    ++num_bytes_written;
                }
            }
        }
        output.close();
    } else {
        std::cout << "Error with opening the input file" << std::endl;
    }
}

